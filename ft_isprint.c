#include "libft.h"

int	ft_isprint(int i)
{
	if ((i >= 40 && i <= 47)
		|| (i >= 50 && i <= 57)
		|| (i >= 60 && i <= 67)
		|| (i >= 70 && i <= 77)
		|| (i >= 100 && i <= 107)
		|| (i >= 110 && i <= 117)
		|| (i >= 120 && i <= 127)
		|| (i >= 130 && i <= 137)
		|| (i >= 140 && i <= 147)
		|| (i >= 150 && i <= 157)
		|| (i >= 160 && i <= 167)
		|| (i >= 170 && i <= 176))
		return (1);
	return (0);
}
